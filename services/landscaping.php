      // ---- Product Swiper ----
      const prodSw = new Swiper('.prodSwiper', {
        slidesPerView: 1.15,
        spaceBetween: 16,
        breakpoints: {
          480: { slidesPerView: 1.6, spaceBetween: 18 },
